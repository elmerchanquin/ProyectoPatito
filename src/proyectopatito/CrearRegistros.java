    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
try{
    Conectar conectar = new Conectar();
    Connection conexionPatito = conectar.conexion();
    
    String paraNombre;
    String paraDireccion;
    String paraTelefono;
    String paraNIT;
    int paraServicio=0;
    String sql = "";
    
    paraNombre = txtNombre.getText();
    paraDireccion = txtDireccion.getText();
    paraTelefono = txtTelefono.getText();
    paraNIT = txtNIT.getText();
    
    if (opcUno.isSelected()){
        paraServicio = 1;
    }else if(opcDos.isSelected()){
        paraServicio = 2;   
    }else if(opcTres.isSelected()){
       paraServicio = 3;
    }
    sql = "insert into clientes (nombre, direccion, telefono, NIT, servicio) values (?, ?, ?, ?, ?)";
    
    PreparedStatement statementPatito = conexionPatito.prepareStatement(sql);
    statementPatito.setString(1, paraNombre);
    statementPatito.setString(2, paraDireccion);
    statementPatito.setString(3, paraTelefono);
    statementPatito.setString(4, paraNIT);
    statementPatito.setInt(5, paraServicio);
       
    int p = statementPatito.executeUpdate();
    if (p > 0){
        JOptionPane.showMessageDialog(null, "El Registro del Cliente fue Guardado");
        
    conexionPatito.close();
    
    }
}catch(SQLException e){
        System.out.println(e.getMessage());
}

txtNombre.setText("");
txtDireccion.setText("");
txtTelefono.setText("");
txtNIT.setText("");
SERVICIOS.clearSelection();

        
    }//GEN-LAST:event_btnGuardarActionPerformed

