    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        try {
            Conectar conectar = new Conectar();
            Connection conexionPatito = conectar.conexion();

            int filtro = Integer.parseInt(txtFiltro.getText());

            JasperReport reporte = null;
            String path = "src\\reportespatito\\ComprobanteCliente.jasper";

            Map parametro = new HashMap();
            parametro.put("documento", filtro);

            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);

            JasperPrint jprint = JasperFillManager.fillReport(reporte, parametro, conexionPatito);

            JasperViewer view = new JasperViewer(jprint, false);

            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);

            view.setVisible(true);

        } catch (JRException ex) {
            Logger.getLogger(ReportesPatito.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

